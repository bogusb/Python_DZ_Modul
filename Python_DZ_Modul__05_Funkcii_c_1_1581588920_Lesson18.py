# из пяти чисел. Числа передаются в качестве параметров.


def smallest_of_five(num1, num2, num3, num4, num5):
    s = num1
    if  s > num2:
        s = num2
    if  s > num3:
        s = num3
    if  s > num4:
        s = num4
    if  s > num5:
        s = num5
    return s


a = smallest_of_five(1, 2, -3, 4, 5)
print(a)  # -3


