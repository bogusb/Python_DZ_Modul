# в качестве параметра и отображает все четные числа
# между ними.


def even(start_range, end_range):
    step = 1
    if start_range > end_range:
        step = -1
    for i in range(start_range, end_range + step, step):
        if i % 2 == 0:
            print(i)


even(15, 9)

'''
14
12
10
'''


