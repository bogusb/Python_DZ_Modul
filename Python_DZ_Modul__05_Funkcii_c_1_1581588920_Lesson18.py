# Напишите функцию, которая отображает пустой или
# заполненный квадрат из некоторого символа. Функция
# принимает в качестве параметров: длину стороны ква-
# драта, символ и переменную логического типа:
# - если она равна True, квадрат заполненный;
# - если False, квадрат пустой.


def square(side_len_int, symbol_str, isfilled_bool):
    for i in range(side_len_int):
        if isfilled_bool or i == 0 or i == (side_len_int - 1):
            print(symbol_str * side_len_int)
        else:
            print(symbol_str + (' ' * (side_len_int - 2)) + symbol_str)


square(4, '7', False)

'''
7777
7  7
7  7
7777
'''


